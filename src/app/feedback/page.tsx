'use client'

import { useState, useEffect, Suspense } from 'react'
import { useSearchParams } from 'next/navigation'
import { Card, CardContent, CardDescription, CardHeader, CardTitle } from "@/components/ui/card"
import { Button } from "@/components/ui/button"
import { Input } from "@/components/ui/input"
import { Label } from "@/components/ui/label"
import { Textarea } from "@/components/ui/textarea"

import { Notification } from "@/components/ui/notification"
import { supabase } from "@/lib/supabase"
import { BRAND_ASSETS, STORE_CONFIG, type StoreId } from "@/lib/constants"
import { Send, Star, ArrowLeft, ArrowRight, Mail, Phone, MapPin } from "lucide-react"
import Image from "next/image"

interface FeedbackForm {
  email: string
  phone: string
  // Store bilgisi
  store_id: string
  store_name: string
  // Adƒ±m 1: Genel Deƒüerlendirme
  overall_rating: number
  // Adƒ±m 2: Market Deƒüerlendirmesi
  product_quality: number
  price_satisfaction: number
  staff_friendliness: number
  cleanliness_rating: number
  shopping_experience: number
  // Adƒ±m 3: Mesaj
  message: string
}

interface RatingProps {
  rating: number
  onRatingChange: (rating: number) => void
  label: string
  required?: boolean
}

// Yƒ±ldƒ±z Rating Komponenti
function StarRating({ rating, onRatingChange, label, required = false }: RatingProps) {
  return (
    <div className="space-y-2">
      <Label className="text-sm font-medium text-gray-900">
        {label} {required && <span className="text-red-500">*</span>}
      </Label>
      <div className="flex items-center gap-1">
        {[1, 2, 3, 4, 5].map((star) => (
          <button
            key={star}
            type="button"
            onClick={() => onRatingChange(star)}
            className="p-1 hover:scale-110 transition-transform"
          >
            <Star
              className={`h-8 w-8 ${
                star <= rating
                  ? 'text-yellow-400 fill-yellow-400'
                  : 'text-gray-300 hover:text-yellow-300'
              }`}
            />
          </button>
        ))}
        <span className="ml-2 text-sm text-gray-600">
          {rating > 0 ? `${rating}/5` : 'Deƒüerlendirin'}
        </span>
      </div>
    </div>
  )
}

function FeedbackContent() {
  const searchParams = useSearchParams()
  const storeParam = searchParams.get('store') as StoreId
  
  // Store bilgisini belirle
  const currentStore = storeParam && STORE_CONFIG[storeParam] 
    ? STORE_CONFIG[storeParam] 
    : STORE_CONFIG.courtyard // Default olarak courtyard

  const [currentStep, setCurrentStep] = useState(1)
  const [form, setForm] = useState<FeedbackForm>({
    email: '',
    phone: '',
    store_id: currentStore.id,
    store_name: currentStore.name,
    overall_rating: 0,
    product_quality: 0,
    price_satisfaction: 0,
    staff_friendliness: 0,
    cleanliness_rating: 0,
    shopping_experience: 0,
    message: ''
  })
  
  const [isSubmitting, setIsSubmitting] = useState(false)
  
  // Notification state
  const [notification, setNotification] = useState<{
    open: boolean
    type: 'success' | 'error' | 'warning'
    title: string
    description?: string
  }>({
    open: false,
    type: 'success',
    title: '',
    description: ''
  })
  
  // Spam korumasƒ± i√ßin
  const [lastSubmitTime, setLastSubmitTime] = useState<number>(0)
  const [submitCount, setSubmitCount] = useState<number>(0)
  const SUBMIT_COOLDOWN = 30000 // 30 saniye cooldown
  const MAX_SUBMISSIONS_PER_DAY = 2 // 24 saatte maksimum 2 g√∂nderim

  // LocalStorage'dan g√∂nderim ge√ßmi≈üini y√ºkle (store bazƒ±nda)
  useEffect(() => {
    const storageKey = `dmar_feedback_submissions_${currentStore.id}`
    const savedSubmissions = localStorage.getItem(storageKey)
    if (savedSubmissions) {
      const submissions = JSON.parse(savedSubmissions)
      const now = Date.now()
      // Son 24 saat i√ßindeki g√∂nderileri filtrele
      const recentSubmissions = submissions.filter((time: number) => now - time < 86400000) // 24 saat = 86400000ms
      setSubmitCount(recentSubmissions.length)
      
      // En son g√∂nderim zamanƒ±nƒ± bul
      if (recentSubmissions.length > 0) {
        setLastSubmitTime(Math.max(...recentSubmissions))
      }
    }
  }, [currentStore.id])

  const handleInputChange = (field: keyof FeedbackForm, value: string | number) => {
    setForm(prev => ({ ...prev, [field]: value }))
  }

  // Notification g√∂sterme fonksiyonlarƒ±
  const showNotification = (type: 'success' | 'error' | 'warning', title: string, description?: string) => {
    setNotification({
      open: true,
      type,
      title,
      description
    })
  }

  // Spam kontrol√º
  const canSubmit = () => {
    const now = Date.now()
    const timeSinceLastSubmit = now - lastSubmitTime
    
    // 30 saniye cooldown kontrol√º
    if (timeSinceLastSubmit < SUBMIT_COOLDOWN) {
      return {
        canSubmit: false,
        reason: `L√ºtfen ${Math.ceil((SUBMIT_COOLDOWN - timeSinceLastSubmit) / 1000)} saniye bekleyin.`
      }
    }
    
    // G√ºnl√ºk limit kontrol√º
    if (submitCount >= MAX_SUBMISSIONS_PER_DAY) {
      return {
        canSubmit: false,
        reason: '24 saat i√ßinde maksimum 2 geri bildirim g√∂nderebilirsiniz. L√ºtfen yarƒ±n tekrar deneyin.'
      }
    }
    
    return { canSubmit: true, reason: '' }
  }

  const handleNext = () => {
    if (currentStep < 2) {
      setCurrentStep(currentStep + 1)
    }
  }

  const handlePrevious = () => {
    if (currentStep > 1) {
      setCurrentStep(currentStep - 1)
    }
  }

  const canProceedFromStep1 = () => {
    return form.product_quality > 0 && 
           form.price_satisfaction > 0 && 
           form.staff_friendliness > 0 && 
           form.cleanliness_rating > 0 && 
           form.shopping_experience > 0
  }

  const handleSubmit = async () => {
    // Spam kontrol√º
    const submitCheck = canSubmit()
    if (!submitCheck.canSubmit) {
      // Spam uyarƒ±sƒ±nƒ± notification olarak g√∂ster
      if (submitCount >= MAX_SUBMISSIONS_PER_DAY) {
        showNotification(
          'warning',
          'G√ºnl√ºk Limit A≈üƒ±ldƒ±',
          '24 saat i√ßinde maksimum 2 geri bildirim g√∂nderebilirsiniz. L√ºtfen yarƒ±n tekrar deneyin.'
        )
      } else {
        showNotification(
          'warning',
          'L√ºtfen Bekleyin',
          `${Math.ceil((SUBMIT_COOLDOWN - (Date.now() - lastSubmitTime)) / 1000)} saniye sonra tekrar deneyebilirsiniz.`
        )
      }
      return
    }

    setIsSubmitting(true)

    try {
      // Email validasyonu (eƒüer girilmi≈üse)
      if (form.email) {
        const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/
        if (!emailRegex.test(form.email)) {
          showNotification('error', 'Ge√ßersiz Email', 'L√ºtfen ge√ßerli bir email adresi girin.')
          setIsSubmitting(false)
          return
        }
      }

      // Rating ortalamalarƒ±nƒ± hesapla
      const averageRating = (
        form.product_quality + 
        form.price_satisfaction + 
        form.staff_friendliness + 
        form.cleanliness_rating + 
        form.shopping_experience
      ) / 5

      // Feedback tipini belirle (ortalama rating'e g√∂re)
      let feedbackType: 'complaint' | 'suggestion' | 'request' = 'suggestion'
      if (averageRating <= 2) {
        feedbackType = 'complaint'
      } else if (averageRating >= 4) {
        feedbackType = 'suggestion'
      } else {
        feedbackType = 'request'
      }

      // Konu olu≈ütur
      const subject = `${currentStore.name} - Market Deƒüerlendirmesi - ${averageRating.toFixed(1)}/5 Yƒ±ldƒ±z`

      // Detaylƒ± mesaj olu≈ütur
      const detailedMessage = `
MARKET DEƒûERLENDƒ∞RMESƒ∞:
üìç ≈ûube: ${currentStore.fullName} (${currentStore.location})

DETAYLI PUANLAR:
‚Ä¢ √úr√ºn Kalitesi: ${form.product_quality}/5 ‚≠ê
‚Ä¢ Fiyat Memnuniyeti: ${form.price_satisfaction}/5 ‚≠ê
‚Ä¢ Personel Dostluƒüu: ${form.staff_friendliness}/5 ‚≠ê
‚Ä¢ Temizlik: ${form.cleanliness_rating}/5 ‚≠ê
‚Ä¢ Alƒ±≈üveri≈ü Deneyimi: ${form.shopping_experience}/5 ‚≠ê

Ortalama Puan: ${averageRating.toFixed(1)}/5 ‚≠ê
${form.message ? `\nMESAJ:\n${form.message}` : ''}
      `.trim()

      // Supabase'e kaydet
      const { error } = await supabase
        .from('complaints')
        .insert([
          {
            full_name: 'Anonim M√º≈üteri', // Ad soyad istenmiyor
            email: form.email || 'anonim@dmar.com', // Email yoksa anonim
            phone: form.phone || null,
            complaint_type: feedbackType,
            subject: subject,
            message: detailedMessage,
            status: 'pending',
            priority: averageRating <= 2 ? 'high' : 'medium',
            store_id: form.store_id,
            store_name: form.store_name,
            store_location: currentStore.location
          }
        ])

      if (error) {
        console.error('Supabase error:', error)
        showNotification('error', 'G√∂nderim Hatasƒ±', 'Geri bildirim g√∂nderilirken bir hata olu≈ütu. L√ºtfen tekrar deneyin.')
        setIsSubmitting(false)
        return
      }

      // Ba≈üarƒ±lƒ± g√∂nderim - LocalStorage'a kaydet (store bazƒ±nda)
      const now = Date.now()
      const storageKey = `dmar_feedback_submissions_${currentStore.id}`
      const savedSubmissions = localStorage.getItem(storageKey)
      let submissions = savedSubmissions ? JSON.parse(savedSubmissions) : []
      
      // Yeni g√∂nderimi ekle
      submissions.push(now)
      
      // Son 24 saat i√ßindeki g√∂nderileri filtrele ve kaydet
      submissions = submissions.filter((time: number) => now - time < 86400000)
      localStorage.setItem(storageKey, JSON.stringify(submissions))
      
      // State'leri g√ºncelle
      setLastSubmitTime(now)
      setSubmitCount(submissions.length)

      // Ba≈üarƒ± notification'ƒ± g√∂ster
      showNotification(
        'success', 
        'G√∂nderim Ba≈üarƒ±lƒ±!', 
        'Geri bildiriminiz ba≈üarƒ±yla g√∂nderildi. Deƒüerli g√∂r√º≈üleriniz i√ßin te≈üekk√ºr ederiz.'
      )

      // Formu temizle
      setForm({
        email: '',
        phone: '',
        store_id: currentStore.id,
        store_name: currentStore.name,
        overall_rating: 0,
        product_quality: 0,
        price_satisfaction: 0,
        staff_friendliness: 0,
        cleanliness_rating: 0,
        shopping_experience: 0,
        message: ''
      })

      // ƒ∞lk adƒ±ma d√∂n
      setCurrentStep(1)

    } catch (error) {
      console.error('Error submitting feedback:', error)
      showNotification(
        'error',
        'Beklenmedik Hata',
        error instanceof Error ? error.message : 'Bir hata olu≈ütu. L√ºtfen tekrar deneyin.'
      )
    } finally {
      setIsSubmitting(false)
    }
  }

  const getStepTitle = () => {
    switch (currentStep) {
      case 1: return 'Market Hizmetleri Deƒüerlendirmesi'
      case 2: return 'Mesaj ve ƒ∞leti≈üim'
      default: return ''
    }
  }

  const getStepDescription = () => {
    switch (currentStep) {
      case 1: return 'Market hizmetlerimizi detaylƒ± olarak deƒüerlendirin'
      case 2: return 'Mesajƒ±nƒ±z ve ileti≈üim bilgileriniz (opsiyonel)'
      default: return ''
    }
  }

  return (
    <div className="min-h-screen bg-gradient-to-br from-gray-50 to-gray-100 py-6 sm:py-12 px-3 sm:px-6 lg:px-8">
      <div className="max-w-2xl mx-auto">
        <div className="text-center mb-6 sm:mb-8">
          <div className="w-16 h-16 sm:w-20 sm:h-20 mx-auto mb-4  overflow-hidden flex items-center justify-center">
            <Image
              src={BRAND_ASSETS.logo}
              alt="DMAR Market Logo"
              width={74}
              height={74}
              className="w-full h-full object-contain"
              priority
            />
          </div>
          <h1 className="text-2xl sm:text-3xl font-bold text-primary mb-2">
            M√º≈üteri Geri Bildirimi
          </h1>
          <div className="flex items-center justify-center gap-2 mb-2">
            <MapPin className="h-4 w-4 sm:h-5 sm:w-5 text-gray-500" />
            <span className="text-base sm:text-lg font-medium text-gray-700">{currentStore.fullName}</span>
          </div>
          <p className="text-sm sm:text-lg text-gray-600">
            {currentStore.location} ≈üubemizin hizmetlerini deƒüerlendirin
          </p>
        </div>

        {/* Progress Bar */}
        <div className="mb-6 sm:mb-8">
          <div className="flex items-center justify-between mb-2">
            <span className="text-xs sm:text-sm font-medium text-gray-700">Adƒ±m {currentStep} / 2</span>
            <span className="text-xs sm:text-sm text-gray-500">{Math.round((currentStep / 2) * 100)}% Tamamlandƒ±</span>
          </div>
          <div className="w-full bg-gray-200 rounded-2xl h-2">
            <div 
              className="bg-primary h-2 rounded-2xl transition-all duration-300"
              style={{ width: `${(currentStep / 2) * 100}%` }}
            ></div>
          </div>
        </div>

        {/* Modern Notification */}
        <Notification
          open={notification.open}
          onOpenChange={(open) => setNotification(prev => ({ ...prev, open }))}
          type={notification.type}
          title={notification.title}
          description={notification.description}
          autoClose={true}
          duration={6000}
        />

        <Card className="shadow-lg border-0 rounded-2xl">
          <CardHeader className="bg-white rounded-t-2xl border-b p-4 sm:p-6">
            <CardTitle className="text-lg sm:text-xl font-semibold text-gray-900">
              {getStepTitle()}
            </CardTitle>
            <CardDescription className="text-sm sm:text-base text-gray-600">
              {getStepDescription()}
            </CardDescription>
          </CardHeader>
          <CardContent className="p-4 sm:p-6">
            {/* Adƒ±m 1: Market Hizmetleri Deƒüerlendirmesi */}
            {currentStep === 1 && (
              <div className="space-y-6">
                <div className="space-y-6">
                  <StarRating
                    rating={form.product_quality}
                    onRatingChange={(rating) => handleInputChange('product_quality', rating)}
                    label="√úr√ºn kalitesi nasƒ±l?"
                    required
                  />
                  
                  <StarRating
                    rating={form.price_satisfaction}
                    onRatingChange={(rating) => handleInputChange('price_satisfaction', rating)}
                    label="Fiyatlardan memnuniyet d√ºzeyiniz?"
                    required
                  />
                  
                  <StarRating
                    rating={form.staff_friendliness}
                    onRatingChange={(rating) => handleInputChange('staff_friendliness', rating)}
                    label="Personelin dostluƒüu ve yardƒ±mseverliƒüi?"
                    required
                  />
                  
                  <StarRating
                    rating={form.cleanliness_rating}
                    onRatingChange={(rating) => handleInputChange('cleanliness_rating', rating)}
                    label="Market temizliƒüi ve d√ºzeni?"
                    required
                  />
                  
                  <StarRating
                    rating={form.shopping_experience}
                    onRatingChange={(rating) => handleInputChange('shopping_experience', rating)}
                    label="Genel alƒ±≈üveri≈ü deneyimi?"
                    required
                  />
                </div>

                <div className="flex justify-end pt-4">
                  <Button
                    onClick={handleNext}
                    disabled={!canProceedFromStep1()}
                    className="bg-gray-900 hover:bg-gray-800 text-white rounded-2xl px-4 sm:px-6 py-2 flex items-center gap-2 text-sm sm:text-base"
                  >
                    ƒ∞leri
                    <ArrowRight className="h-4 w-4 sm:h-5 sm:w-5" />
                  </Button>
                </div>
              </div>
            )}

            {/* Adƒ±m 2: Mesaj ve ƒ∞leti≈üim */}
            {currentStep === 2 && (
              <div className="space-y-6">
                <div className="space-y-2">
                  <Label htmlFor="message" className="text-sm font-medium text-gray-900">
                    G√∂r√º≈ü, √∂neri veya ≈üikayetiniz (Opsiyonel)
                  </Label>
                  <Textarea
                    id="message"
                    value={form.message}
                    onChange={(e: React.ChangeEvent<HTMLTextAreaElement>) => handleInputChange('message', e.target.value)}
                    placeholder="Deneyiminizi, √∂nerilerinizi veya ≈üikayetlerinizi detaylƒ± olarak yazabilirsiniz..."
                    rows={5}
                    className="rounded-2xl border-2 border-gray-300 focus:border-gray-900 focus:ring-gray-900 resize-none text-base"
                    style={{ fontSize: '16px' }}
                  />
                  <p className="text-xs text-gray-500">
                    ƒ∞steƒüe baƒülƒ± olarak ek mesajƒ±nƒ±zƒ± yazabilirsiniz.
                  </p>
                </div>



                {/* ƒ∞leti≈üim Bilgileri */}
                <div className="border-t pt-6">
                  <h4 className="text-sm font-medium text-gray-900 mb-4">
                    ƒ∞leti≈üim Bilgileri (Opsiyonel)
                  </h4>
                  <div className="grid gap-4 md:grid-cols-2">
                    <div className="space-y-2">
                      <Label htmlFor="email" className="text-sm font-medium text-gray-900">
                        E-posta Adresi
                      </Label>
                      <div className="relative">
                        <Mail className="absolute left-3 top-1/2 transform -translate-y-1/2 h-4 w-4 text-gray-400" />
                        <Input
                          id="email"
                          type="email"
                          value={form.email}
                          onChange={(e) => handleInputChange('email', e.target.value)}
                          placeholder="ornek@email.com"
                          className="pl-10 rounded-2xl border-2 border-gray-300 focus:border-gray-900 focus:ring-gray-900 text-base"
                          style={{ fontSize: '16px' }}
                        />
                      </div>
                      <p className="text-xs text-gray-500">
                        Size d√∂n√º≈ü yapmamƒ±z i√ßin e-posta adresinizi payla≈üabilirsiniz.
                      </p>
                    </div>

                    <div className="space-y-2">
                      <Label htmlFor="phone" className="text-sm font-medium text-gray-900">
                        Telefon Numarasƒ±
                      </Label>
                      <div className="relative">
                        <Phone className="absolute left-3 top-1/2 transform -translate-y-1/2 h-4 w-4 text-gray-400" />
                        <Input
                          id="phone"
                          type="tel"
                          value={form.phone}
                          onChange={(e) => handleInputChange('phone', e.target.value)}
                          placeholder="0555 123 45 67"
                          className="pl-10 rounded-2xl border-2 border-gray-300 focus:border-gray-900 focus:ring-gray-900 text-base"
                          style={{ fontSize: '16px' }}
                        />
                      </div>
                      <p className="text-xs text-gray-500">
                        Acil durumlar i√ßin telefon numaranƒ±zƒ± payla≈üabilirsiniz.
                      </p>
                    </div>
                  </div>
                </div>



                <div className="flex flex-col sm:flex-row justify-between gap-3 pt-4">
                  <Button
                    onClick={handlePrevious}
                    variant="outline"
                    className="rounded-2xl px-4 sm:px-6 py-2 flex items-center gap-2 border-gray-300 text-gray-700 hover:bg-gray-50 text-sm sm:text-base order-2 sm:order-1"
                  >
                    <ArrowLeft className="h-4 w-4" />
                    Geri
                  </Button>
                  
                  <Button
                    onClick={handleSubmit}
                    disabled={isSubmitting}
                    className="bg-gray-900 hover:bg-gray-800 text-white rounded-2xl px-4 sm:px-6 py-2 flex items-center gap-2 disabled:opacity-50 disabled:cursor-not-allowed text-sm sm:text-base order-1 sm:order-2"
                  >
                    {isSubmitting ? (
                      <>
                        <div className="animate-spin rounded-full h-4 w-4 border-b-2 border-white mr-2"></div>
                        <span className="hidden sm:inline">G√∂nderiliyor...</span>
                        <span className="sm:hidden">G√∂nderiliyor</span>
                      </>
                    ) : (
                      <>
                        <Send className="h-4 w-4" />
                        G√∂nder
                      </>
                    )}
                  </Button>
                </div>
              </div>
            )}
          </CardContent>
        </Card>

        <div className="mt-6 sm:mt-8 text-center">
          <p className="text-xs sm:text-sm text-gray-500 leading-relaxed">
            Geri bildiriminiz gizli tutulacak ve deƒüerlendirilerek size d√∂n√º≈ü yapƒ±lacaktƒ±r.
            <br className="hidden sm:block" />
            <span className="block sm:inline mt-1 sm:mt-0">{currentStore.fullName} - M√º≈üteri memnuniyeti √∂nceliƒüimizdir.</span>
          </p>
        </div>
      </div>
    </div>
  )
}

export default function FeedbackPage() {
  return (
    <Suspense fallback={<div className="flex items-center justify-center min-h-screen">Y√ºkleniyor...</div>}>
      <FeedbackContent />
    </Suspense>
  )
}